    # ── Generic escape hatch ──────────────────────────────────────────────

    async def execute_method(
        self,
        model: str,
        method: str,
        args: Optional[List[Any]] = None,
        kwargs: Optional[Dict[str, Any]] = None,
    ) -> Any:
        """Call any Odoo model method via ``execute_kw``.

        OCP-friendly escape hatch: callers can reach any Odoo model
        (``account.payment``, ``hr.leave``, ``mail.message`` …) without
        modifying this connector. Adding domain-specific helpers above
        remains preferred when the model is used by ≥ 2 callers.
        """
        if not model or not method:
            raise ValueError("model and method are required")
        return await with_retry(
            self.http_client.execute_kw,
            model,
            method,
            list(args or []),
            dict(kwargs or {}),
        )

    # ── Lifecycle ─────────────────────────────────────────────────────────

    async def aclose(self) -> None:
        """Release any cached state held by the HTTP client."""
        self.http_client.clear_uid_cache()

    async def __aenter__(self) -> "OdooConnector":
        return self

    async def __aexit__(self, *_: object) -> None:
        await self.aclose()
