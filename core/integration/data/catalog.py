# ── Helper functions ──────────────────────────────────────────────────

def get_all_providers() -> List[Dict[str, Any]]:
    """Return a flat list of providers with metadata."""
    providers = []
    for provider_key, services in SERVICE_CATALOG.items():
        # Skip non-dict entries (e.g. _required_config_fields lists)
        service_entries = {k: v for k, v in services.items() if isinstance(v, dict)}
        if not service_entries:
            continue
        first_service = next(iter(service_entries.values()))
        providers.append({
            "provider": provider_key,
            "display_name": provider_key.replace("_", " ").title(),
            "service_count": len(service_entries),
            "categories": list({s.get("category", "") for s in service_entries.values()}),
            "logo_url": first_service.get("logo_url", ""),
        })
    return sorted(providers, key=lambda p: p["display_name"])


def get_provider_services(provider: str) -> List[Dict[str, Any]]:
    """Return services for a given provider."""
    services = SERVICE_CATALOG.get(provider, {})
    result = []
    for service_key, meta in services.items():
        if not isinstance(meta, dict):
            continue
        result.append({
            "provider": provider,
            "service": service_key,
            "service_key": service_key,
            **meta,
        })
    return sorted(result, key=lambda s: s["display_name"])


def get_service_detail(provider: str, service: str) -> CatalogEntry | None:
    """Return detailed metadata for a specific service.

    Also merges in provider-level `_required_config_fields` (if defined) so the
    planning prompt can inject them as mandatory install fields.
    """
    provider_data = SERVICE_CATALOG.get(provider, {})
    entry = provider_data.get(service)
    if not entry:
        return None
    # Merge provider-level required config fields (keyed with leading _) into the entry
    provider_required = provider_data.get("_required_config_fields", [])
    result = {"provider": provider, "service": service, "service_key": service, **entry}
    if provider_required:
        result["required_config_fields"] = provider_required
    return result
